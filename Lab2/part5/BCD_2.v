module BCD_2 (
    input  [3:0] A, B,
    input cin,
    output [3:0] T,
    output  cout,
    output [3:0] S0, S1,
    output [6:0] Hex0, Hex1, Hex2, Hex3
);

assign T = A + B + cin;

wire [3:0] z;
assign z    = (T > 4'd9) ? 4'd10 : 4'd0;
assign cout = (T > 4'd9) ? 1'b1  : 1'b0;

assign S0 = T - z;
assign S1 = {3'b000, cout};

// HEX0 = S0
assign Hex0 = 
    (S0 == 4'd0) ? 7'b1000000 :
    (S0 == 4'd1) ? 7'b1111001 :
    (S0 == 4'd2) ? 7'b0100100 :
    (S0 == 4'd3) ? 7'b0110000 :
    (S0 == 4'd4) ? 7'b0011001 :
    (S0 == 4'd5) ? 7'b0010010 :
    (S0 == 4'd6) ? 7'b0000010 :
    (S0 == 4'd7) ? 7'b1111000 :
    (S0 == 4'd8) ? 7'b0000000 :
    (S0 == 4'd9) ? 7'b0010000 :
                  7'b1111111;  // default (blank)

// HEX1 = S1
assign Hex1 = 
    (S1 == 4'd0) ? 7'b1000000 :
    (S1 == 4'd1) ? 7'b1111001 :
                  7'b1111111;  // only show 0 or 1

// HEX2 = B
assign Hex2 = 
    (B == 4'd0) ? 7'b1000000 :
    (B == 4'd1) ? 7'b1111001 :
    (B == 4'd2) ? 7'b0100100 :
    (B == 4'd3) ? 7'b0110000 :
    (B == 4'd4) ? 7'b0011001 :
    (B == 4'd5) ? 7'b0010010 :
    (B == 4'd6) ? 7'b0000010 :
    (B == 4'd7) ? 7'b1111000 :
    (B == 4'd8) ? 7'b0000000 :
    (B == 4'd9) ? 7'b0010000 :
                 7'b1111111;

// HEX3 = A
assign Hex3 = 
    (A == 4'd0) ? 7'b1000000 :
    (A == 4'd1) ? 7'b1111001 :
    (A == 4'd2) ? 7'b0100100 :
    (A == 4'd3) ? 7'b0110000 :
    (A == 4'd4) ? 7'b0011001 :
    (A == 4'd5) ? 7'b0010010 :
    (A == 4'd6) ? 7'b0000010 :
    (A == 4'd7) ? 7'b1111000 :
    (A == 4'd8) ? 7'b0000000 :
    (A == 4'd9) ? 7'b0010000 :
                 7'b1111111;

endmodule


