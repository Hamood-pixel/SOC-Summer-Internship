module comparator ( input [3:0] V, output z,output [3:0] mux_op, output [6:0] d0,d1);


assign z = V[3] & (V[2] | V[1]);
 
wire [3:0] A = 
 (V == 4'b1010) ? 4'b0000:
 (V == 4'b1011) ? 4'b0001:
 (V == 4'b1100) ? 4'b0010:
 (V == 4'b1101) ? 4'b0011: 
 (V == 4'b1110) ? 4'b0100:
 (V == 4'b1111) ? 4'b0101:
                  4'b0000;
assign d1 = 
  (z == 1) ? 7'b0000110:
             7'b0111111;
 
assign mux_op = (z==1) ? A : V;

assign d0 = 
 (A == 4'b0000 || V == 4'b0000) ? 7'b0111111: //0
 (A == 4'b0001 || V == 4'b0001) ? 7'b0000110: //1
 (A == 4'b0010 || V == 4'b0010) ? 7'b1011011: //2 
 (A == 4'b0011 || V == 4'b0011) ? 7'b1001111: //3
 (A == 4'b0100 || V == 4'b0100) ? 7'b1100110: //4
 (A == 4'b0101 || V == 4'b0101) ? 7'b1101101: //5
 (A == 4'b0110 || V == 4'b0110) ? 7'b1111101: //6
 (A == 4'b0111 || V == 4'b0111) ? 7'b0000111: //7
 (A == 4'b1000 || V == 4'b1000) ? 7'b1111111: //8
 (A == 4'b1001 || V == 4'b1001) ? 7'b1101111: //9
                                 7'b0000000; //blank for rest, don't cares 
											
endmodule 



